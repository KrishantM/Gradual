export const volunteeringConnector: OpportunityConnector = {
  name: 'volunteering',
  supportedTypes: ['volunteering'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const live = await fetchSeekVolunteer();
      opportunities.push(...live);
      console.log(`[VolunteeringConnector] Scraped ${live.length} from Seek Volunteer`);
    } catch (err) {
      errors.push(`SeekVolunteer: ${String(err)}`);
    }

    opportunities.push(...getCuratedVolunteering());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'volunteering',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
