async function fetchSeekVolunteer(): Promise<Opportunity[]> {
  try {
    const res = await fetch('https://seekvolunteer.co.nz/volunteering?sort=newest', {
      headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' },
    });
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const opportunities: Opportunity[] = [];

    $('.search-results__item, .volunteer-card, article.listing, [class*="listing"], [class*="volunteer"]').each((_i, el) => {
      const $el = $(el);
      const title = $el.find('h2, h3, .title, .listing-title, a[class*="title"]').first().text().trim();
      const org = $el.find('.organisation, .company, .org-name, [class*="org"]').first().text().trim();
      const location = $el.find('.location, .suburb, [class*="location"]').first().text().trim();
      const link = $el.find('a[href*="/volunteering/"]').first().attr('href') || $el.find('a').first().attr('href') || '';
      const desc = $el.find('.description, .summary, p').first().text().trim();

      if (!title || !link || title.length < 3) return;
      const fullUrl = link.startsWith('http') ? link : `https://seekvolunteer.co.nz${link}`;

      opportunities.push({
        id: `seekvol_${link.replace(/[^a-z0-9]/gi, '_').slice(0, 60)}`,
        type: 'volunteering',
        source: 'seekvolunteer',
        sourceId: link,
        sourceUrl: fullUrl,
        canonicalUrl: fullUrl,
        title,
        organization: org || 'Seek Volunteer NZ',
        description: desc || title,
        location: location || 'New Zealand',
        locationType: 'onsite',
        city: location || undefined,
        country: 'New Zealand',
        tags: ['volunteering', 'community', 'new-zealand'],
        category: 'Volunteering',
        url: fullUrl,
        applicationType: 'external',
        status: 'active',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      });
    });
    return opportunities;
  } catch (err) {
    console.error('[VolunteeringConnector] Seek Volunteer fetch failed:', err);
    return [];
  }
}

