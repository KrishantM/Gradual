async function fetchGradNZInternships(): Promise<Opportunity[]> {
  try {
    const res = await fetch('https://gradnewzealand.nz/internships', {
      headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' },
    });
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const internships: Opportunity[] = [];

    $('a[href*="/internship"], a[href*="/graduate-job"]').each((_i, el) => {
      const title = $(el).text().trim();
      const href = $(el).attr('href') || '';
      if (!title || title.length < 10 || title.length > 200) return;
      if (['Browse', 'Search', 'Sign in', 'Register', 'Home'].some(s => title.includes(s))) return;

      const fullUrl = href.startsWith('http') ? href : `https://gradnewzealand.nz${href}`;
      const org = $(el).closest('.listing, .card, article').find('.company, .employer, .org').text().trim() || 'Unknown';

      internships.push({
        id: `gradnz_intern_${title.toLowerCase().replace(/[^a-z0-9]/g, '_').slice(0, 60)}`,
        type: 'internship',
        source: 'gradnewzealand',
        sourceId: href,
        sourceUrl: fullUrl,
        title,
        organization: org,
        description: `${title} — an internship listed on GradNewZealand. Visit the listing for full details on requirements and how to apply.`,
        location: 'New Zealand',
        country: 'New Zealand',
        tags: ['internship', 'graduate', 'new-zealand'],
        category: 'Internships',
        url: fullUrl,
        applicationType: 'external',
        status: 'active',
        createdAt: new Date().toISOString(),
      });
    });
    return internships;
  } catch (err) {
    console.error('[InternshipsConnector] GradNZ scrape failed:', err);
    return [];
  }
}

