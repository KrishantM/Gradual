export const internshipsConnector: OpportunityConnector = {
  name: 'internships',
  supportedTypes: ['internship'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const gradNZ = await fetchGradNZInternships();
      opportunities.push(...gradNZ);
      console.log(`[InternshipsConnector] Scraped ${gradNZ.length} from GradNewZealand`);
    } catch (err) {
      errors.push(`GradNZ: ${String(err)}`);
    }

    try {
      const prosple = await fetchProspleNZInternships();
      opportunities.push(...prosple);
      console.log(`[InternshipsConnector] Scraped ${prosple.length} from Prosple NZ`);
    } catch (err) {
      errors.push(`Prosple: ${String(err)}`);
    }

    opportunities.push(...getCuratedInternships());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'internships',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
