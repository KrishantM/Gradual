export const clubsConnector: OpportunityConnector = {
  name: 'clubs',
  supportedTypes: ['club'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const live = await fetchAUTSAClubs();
      opportunities.push(...live);
      console.log(`[ClubsConnector] Scraped ${live.length} clubs from AUTSA`);
    } catch (err) {
      errors.push(`AUTSA: ${String(err)}`);
    }

    opportunities.push(...getCuratedClubs());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'clubs',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
