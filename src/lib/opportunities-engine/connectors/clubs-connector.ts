async function fetchAUTSAClubs(): Promise<Opportunity[]> {
  try {
    const res = await fetch('https://autsa.org.nz/clubs', {
      headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' },
    });
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const clubs: Opportunity[] = [];

    $('h2').each((_i, el) => {
      const name = $(el).text().trim();
      if (!name || name.length < 3 || name.length > 200) return;
      if (['Resources & Training', 'Important Documents', 'Check out our clubs!'].some(s => name.includes(s))) return;

      const parent = $(el).closest('div, section, article');
      const link = parent.find('a[href*="clubs-and-communities"]').attr('href') || '';
      const fullUrl = link.startsWith('http') ? link : link ? `https://www.autsa.org.nz${link}` : 'https://autsa.org.nz/clubs';

      clubs.push({
        id: `autsa_${name.toLowerCase().replace(/[^a-z0-9]/g, '_').slice(0, 60)}`,
        type: 'club',
        source: 'autsa',
        sourceId: name,
        sourceUrl: fullUrl,
        canonicalUrl: fullUrl,
        title: name,
        organization: 'AUT Students\' Association (AUTSA)',
        organizationUrl: 'https://autsa.org.nz',
        description: `${name} is a student club at Auckland University of Technology. Join to connect with like-minded students, build skills, and get involved on campus.`,
        location: 'Auckland, New Zealand',
        locationType: 'onsite',
        city: 'Auckland',
        country: 'New Zealand',
        tags: ['club', 'student', 'aut', 'auckland'],
        category: 'Student Clubs',
        url: fullUrl,
        applicationType: 'external',
        status: 'active',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      });
    });
    return clubs;
  } catch (err) {
    console.error('[ClubsConnector] AUTSA scrape failed:', err);
    return [];
  }
}

