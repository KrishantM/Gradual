export const competitionsConnector: OpportunityConnector = {
  name: 'competitions',
  supportedTypes: ['competition'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const live = await fetchDevpostHackathons();
      opportunities.push(...live);
      console.log(`[CompetitionsConnector] Scraped ${live.length} from DevPost`);
    } catch (err) {
      errors.push(`DevPost: ${String(err)}`);
    }

    opportunities.push(...getCuratedCompetitions());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'competitions',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
