async function fetchDevpostHackathons(): Promise<Opportunity[]> {
  try {
    const res = await fetch('https://devpost.com/hackathons?status=open&order_by=deadline', {
      headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' },
    });
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const hackathons: Opportunity[] = [];

    $('.hackathon-tile, [data-hackathon-tile], .hackathons-container a[href*="devpost.com"]').each((_i, el) => {
      const $el = $(el);
      const title = $el.find('h2, h3, .title, .hackathon-title').first().text().trim();
      const link = $el.attr('href') || $el.find('a').first().attr('href') || '';
      const org = $el.find('.host, .organizer, .organization').first().text().trim();
      const desc = $el.find('.tagline, .description, p').first().text().trim();
      const prize = $el.find('.prize, .prizes').first().text().trim();

      if (!title || !link) return;
      const fullUrl = link.startsWith('http') ? link : `https://devpost.com${link}`;

      hackathons.push({
        id: `devpost_${link.replace(/[^a-z0-9]/gi, '_').slice(0, 60)}`,
        type: 'competition',
        source: 'devpost',
        sourceId: link,
        sourceUrl: fullUrl,
        title,
        organization: org || 'DevPost Hackathon',
        description: desc || title,
        summary: prize ? `Prize pool: ${prize}` : undefined,
        location: 'Online',
        locationType: 'remote',
        isRemote: true,
        country: 'International',
        tags: ['hackathon', 'competition', 'coding', 'innovation'],
        skills: ['programming', 'teamwork', 'innovation'],
        category: 'Competitions',
        url: fullUrl,
        applicationType: 'external',
        status: 'active',
        createdAt: new Date().toISOString(),
        compensation: prize ? { type: 'paid', description: prize } : undefined,
      });
    });
    return hackathons.slice(0, 15);
  } catch (err) {
    console.error('[CompetitionsConnector] DevPost fetch failed:', err);
    return [];
  }
}

