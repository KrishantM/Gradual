export const eventsConnector: OpportunityConnector = {
  name: 'events',
  supportedTypes: ['event'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const live = await fetchEventbriteNZ();
      opportunities.push(...live);
      console.log(`[EventsConnector] Scraped ${live.length} from Eventbrite`);
    } catch (err) {
      errors.push(`Eventbrite: ${String(err)}`);
    }

    opportunities.push(...getCuratedEvents());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'events',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
