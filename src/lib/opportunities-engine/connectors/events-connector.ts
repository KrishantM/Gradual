async function fetchEventbriteNZ(): Promise<Opportunity[]> {
  try {
    const res = await fetch(
      'https://www.eventbrite.co.nz/d/new-zealand/student-events/?page=1',
      { headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' } }
    );
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const events: Opportunity[] = [];

    $('script[type="application/ld+json"]').each((_i, el) => {
      try {
        const data = JSON.parse($(el).text());
        const items = Array.isArray(data) ? data : [data];
        for (const item of items) {
          if (item['@type'] !== 'Event') continue;
          events.push({
            id: `eventbrite_${item.url?.split('-').pop() || Date.now()}`,
            type: 'event',
            source: 'eventbrite',
            sourceId: item.url?.split('-').pop() || '',
            sourceUrl: item.url || '',
            title: item.name || '',
            organization: item.organizer?.name || 'Eventbrite Event',
            description: item.description || item.name || '',
            location: item.location?.name || item.location?.address?.addressLocality || 'New Zealand',
            locationType: item.eventAttendanceMode?.includes('Online') ? 'remote' : 'onsite',
            isRemote: item.eventAttendanceMode?.includes('Online'),
            city: item.location?.address?.addressLocality,
            country: item.location?.address?.addressCountry || 'New Zealand',
            tags: ['event', 'student', 'networking'],
            category: 'Events',
            url: item.url || '#',
            applicationType: 'external',
            status: 'active',
            createdAt: new Date().toISOString(),
            startDate: item.startDate,
            endDate: item.endDate,
            dates: { startDate: item.startDate, endDate: item.endDate },
          });
        }
      } catch { /* skip malformed JSON-LD */ }
    });
    return events;
  } catch (err) {
    console.error('[EventsConnector] Eventbrite fetch failed:', err);
    return [];
  }
}

