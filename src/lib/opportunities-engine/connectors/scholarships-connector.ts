async function fetchUoAScholarships(): Promise<Opportunity[]> {
  try {
    const res = await fetch(
      'https://www.auckland.ac.nz/en/study/scholarships-and-awards/scholarship-types/undergraduate-scholarships.html',
      { headers: { 'User-Agent': 'Gradual-App/2.0', Accept: 'text/html' } }
    );
    if (!res.ok) return [];
    const html = await res.text();
    const $ = cheerio.load(html);
    const scholarships: Opportunity[] = [];

    $('a[href*="scholarships-and-awards"]').each((_i, el) => {
      const title = $(el).text().trim();
      const href = $(el).attr('href') || '';
      if (!title || title.length < 10 || title.length > 200) return;
      if (['Find a scholarship', 'Scholarship types', 'How to apply', 'Search', 'Browse'].some(s => title.includes(s))) return;

      const fullUrl = href.startsWith('http') ? href : `https://www.auckland.ac.nz${href}`;

      scholarships.push({
        id: `uoa_schol_${title.toLowerCase().replace(/[^a-z0-9]/g, '_').slice(0, 60)}`,
        type: 'scholarship',
        source: 'uoa',
        sourceId: href,
        sourceUrl: fullUrl,
        title,
        organization: 'University of Auckland',
        organizationUrl: 'https://www.auckland.ac.nz',
        description: `${title} — a scholarship available at the University of Auckland. Check eligibility and application deadlines on the university website.`,
        location: 'Auckland, New Zealand',
        locationType: 'onsite',
        city: 'Auckland',
        country: 'New Zealand',
        tags: ['scholarship', 'university', 'auckland', 'financial-support'],
        category: 'Scholarships',
        url: fullUrl,
        applicationType: 'external',
        status: 'active',
        createdAt: new Date().toISOString(),
      });
    });
    return scholarships;
  } catch (err) {
    console.error('[ScholarshipsConnector] UoA scrape failed:', err);
    return [];
  }
}

