export const scholarshipsConnector: OpportunityConnector = {
  name: 'scholarships',
  supportedTypes: ['scholarship'],
  async fetch(config): Promise<ConnectorResult> {
    const errors: string[] = [];
    let opportunities: Opportunity[] = [];

    try {
      const live = await fetchUoAScholarships();
      opportunities.push(...live);
      console.log(`[ScholarshipsConnector] Scraped ${live.length} from UoA`);
    } catch (err) {
      errors.push(`UoA: ${String(err)}`);
    }

    opportunities.push(...getCuratedScholarships());

    if (config.maxResults) {
      opportunities = opportunities.slice(0, config.maxResults);
    }

    return {
      opportunities,
      source: 'scholarships',
      fetchedAt: new Date().toISOString(),
      stats: { fetched: opportunities.length, validated: opportunities.length, errors },
    };
  },
};
